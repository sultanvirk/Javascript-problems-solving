
let x=2;
let y=3;

// Swap values of two variables using 3rd variable
let temp = x;
x=y;
y=temp;

console.log(`x = ${x} and y = ${y}`)

// swaping without using 3rd variable


let a=2;
let b=3;
a=a+b    // 2+3 = 5
b=a-b    // 5-3 = 2
a=a-b    // 5-2 = 3

console.log(`Swap without 3rd variable  a = ${a} and b = ${b}`)